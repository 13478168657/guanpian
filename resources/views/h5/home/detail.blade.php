            <h3 class="headNew pa">我省力保雄安新区起步区安全度汛</h3>
            <article class="pd10">
                <div class="info">发布时间:<time>2018-06-07</time>&nbsp;&nbsp;浏览: <script src="js/count.php" type='text/javascript' language="javascript"></script> 次&nbsp;&nbsp;作者:雄安热线</div>
                <div class="ad_wap"></div>
                <div class="content">
                    <p><p>
                        　　日前，记者从省水利厅了解到，立足防大汛、抗大洪、抢大险、救大灾，我省扎实做好各项防汛准备工作，确保雄安新区起步区度汛安全。</p>
                    <p>
                        　　<a href="http://www.xaxq365.com.cn"><strong>雄安新区</strong></a>起步区位于白洋淀新安北堤以北区域，其周边的防洪工程包括白洋淀千里堤、新安北堤、萍河左堤、南拒马河右堤、白沟引河右堤及赵王新河。雄安新区起步区的防洪任务主要是通过确保周边堤防安全，保障起步区不受外部洪水威胁，同时通过自排、机排防止出现严重内涝。由于地势低洼，现有工程防洪能力偏低。为确保今年雄安新区安全度汛，省水利厅编制完成了《2018年雄安新区起步区安全度汛方案》，已上报国家防总。</p>
                    <p>
                        　　为全力做好雄安新区起步区今年的防汛工作，省、市、县进行了全面防汛大检查。通过检查，安新、容城、雄县3县共发现安全隐患8处，现已全部完成整改。结合全省河湖清理行动，省水利厅联合省委督查室、省政府督查室以及省直有关部门组成联合督查组，对全省河湖清理工作进行了重点督查。截至目前，白洋淀及上游河道清理垃圾36.9万立方米，整治排污口10处，清除涉河违障38处，治理黑臭水体28条段，回填沙坑2万立方米，提高了河道行洪能力;对河湖清理行动中存在的问题，采取&ldquo;一市一单&rdquo;的方式，加速推进各市整改落实。</p>
                    <p>
                        　　在全面落实各项防汛责任的基础上，我省进一步压实乡镇、村组(社区)等基层防汛责任，建立完善群测群防预警监测体系，确保责任落实&ldquo;全覆盖&rdquo;;督导加强对县、乡、村级各类防汛责任人业务培训，提升履职能力。同时，依托大清河系上游山区山洪灾害预警系统，加强监测预警，一旦发生洪水，能够及时提醒，提前组织人员转移。主汛期前完成白洋淀上游主要河道20处视频监控站建设，其中5个分洪口门将全部进行视频监控，提升监测预警能力。对雄安新区上游6座大型及8座中型水库，精准预报，科学调度，如遇下游抢险，在不影响水库安全的前提下，拦洪错峰，减轻下游压力。</p>
                    </p>
